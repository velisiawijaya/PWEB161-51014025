	<h2>
	Krisen<br>
Gereja (bahasa Portugis: igreja; bahasa Yunani: εκκλησία (ekklêsia); bahasa Inggris: Church) adalah suatu kata bahasa Indonesia yang berarti suatu perkumpulan atau lembaga dari penganut iman Kristiani. Istilah Yunani ἐκκλησία, yang muncul dalam Perjanjian Baru di Alkitab Kristen biasanya diterjemahkan sebagai "jemaat/umat". Istilah ini muncul dalam 2 ayat dari Injil Matius, 24 ayat dari Kisah Para Rasul, 58 ayat dari surat Rasul Paulus, 2 ayat dari Surat kepada Orang Ibrani, 1 ayat dari Surat Yakobus, 3 ayat dari Surat Yohanes yang Ketiga, dan 19 ayat dari Kitab Wahyu.<br><br>

Kata "Gereja" merupakan kata ambilan dari bahasa Portugis: igreja, yang berasal dari bahasa Yunani: εκκλησία (ekklêsia) yang berarti dipanggil keluar (ek= keluar; klesia dari kata kaleo= memanggil); kumpulan orang yang dipanggil ke luar dari dunia memiliki beberapa arti:

Arti pertama ialah 'umat', atau lebih tepat, 'persekutuan' orang Kristen. Arti ini diterima sebagai arti pertama bagi orang Kristen. Jadi, gereja pertama-tama bukanlah sebuah gedung.
Arti kedua adalah sebuah perhimpunan atau pertemuan ibadah umat Kristen. Bisa bertempat di rumah kediaman, lapangan, ruangan di hotel, maupun tempat rekreasi.
Arti ketiga ialah mazhab (aliran) atau denominasi dalam agama Kristen. Gereja Katolik, Gereja Protestan, dan lain-lain.
Arti keempat ialah lembaga (administratif) daripada sebuah mazhab Kristen. Contoh kalimat “Gereja menentang perang Irak”.
Arti terakhir dan juga arti umum adalah sebuah “rumah ibadah” umat Kristen, di mana umat bisa berdoa atau bersembahyang.
Gereja (untuk arti yang pertama) terbentuk 50 hari setelah kebangkitan Yesus Kristus pada hari raya Pentakosta, yaitu ketika Roh Kudus yang dijanjikan Allah diberikan kepada semua yang percaya pada Yesus Kristus.<br><br>


